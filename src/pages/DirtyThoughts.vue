          The desired green roof growing media can vary widely depending upon
          usage. Storm water control requires something different than what a
          wetlands would require. Intensive mix blends with their deeper depths
          are typically not the same as extensive mixes. Thus, proper components
          and component ratios are crucial to achieve desired performance. How
          do suppliers come up with mixes to meet project needs? It depends on
          the specifications, and a well written specification is critical to
          project success.
        </p>
        <p>
          Growing media specifications can be divided into two broad types.
          “Material Specifications” usually provide for what components to use
          and how much of each component goes into the mix. “Performance
          Specifications” will typically focus less on specifying particular
          materials, and more on detailing what the performance of the blend
          should be (permeability, wet weight, water retention, etc.). Material
          specifications may be appropriate for proprietary blends or when there
          is intimate knowledge of the available local materials. Performance
          specifications are often preferred because they allow for some
          flexibility in materials and focus on the characteristics of the final
          blended material.
        </p>
        <p>
          The suppliers/blenders were somewhat split on which type of
          specifications are preferred. Anderson has a preference for
          performance specs, indicating that these types of specs allow for the
          best mixes to be prepared from locally available materials. He
          indicated that his company will initially prepare small batches of
          media, have them tested for performance, then adjust mix based on test
          results. Duprey likes specs that provide an exact blend recipe, but he
          also uses routine testing to verify mixes meet specifications. All
          agreed that specifications must take into account regional differences
          in sands, composts, and other components, as well as, budgetary
          limitiations.
        </p>
        <p>
          Regardless of the specification type, there should be a component
          detailing how the blended green roof growing media is expected to
          perform. The blended material is, after all, what’s going up on the
          roof. Both Hayden and Bruce indicated that it is imperative to know
          what type of performance is expected from the roof before a proper
          specification and resultant growing media can be prepared.
          Unfortunately some designers and roofing professionals don’t have a
          firm grasp on what is meant by performance. Bruce indicated that
          factors such as, type of vegetation, water management, material depth,
          load limits, and intensity of use must be considered.
